module LuasLingkaran where
    -- LuasLingkaran - luasLingkaran(r) 
    -- Definisi dan Spesifikasi 
    luasLingkaran :: Float -> Float
        -- luasLingkaran(r) menghasilkan luas lingkaran berdasarkan rumus: luas = 3.1415 * r * r
    -- Realisasi
    luasLingkaran r = 3.1415 * r * r
    -- Contoh aplikasi
    -- luasLingkaran 2
    -- 12.566
    -- luasLingkaran 3.5
    -- 38.483376